void  LCD_CrtlWrite_Area(uint8_t SX,uint8_t EX,uint16_t SY,uint16_t EY)
{
	SdCmd(0x2A);
	SdData((u8)(SX/(u16)256));
	SdData((u8)(SX%(u16)256));
	SdData((u8)(EX/(u16)256));
	SdData((u8)(EX%(u16)256));
	SdCmd((u8)0x2B);
	SdData((uint8_t)(SY/(u16)256));
	SdData((u8)(SY%(u16)256));
	SdData((uint8_t)(EY/(u16)256));
	SdData((u8)(EY%(u16)256));
	SdCmd((u8)0x2C);	
}  


 //画矩形
void Draw_Rectangle(uint8_t Start_X, uint16_t Start_Y,uint8_t End_X, uint16_t End_Y,uint8_t color, uint8_t LineW)
{
	uint16_t i,j,w,h;
	w=(uint16_t)End_X-(uint16_t)Start_X;
	h=End_Y-Start_Y;
	//画第一条线
	LCD_CrtlWrite_Area(Start_X,End_X,Start_Y,Start_Y+(u16)LineW-(u16)1);
	for (j=0; j<LineW;j++)
	{
		for(i=0;i<=w;i++)
		{
			LCD_CtrlWrite_Color(color);
		}
	}
	LCD_CrtlWrite_Area(Start_X,End_X,(End_Y-(u16)LineW)+(u16)1,End_Y);
	for (j=0; j<LineW;j++)
	{
		for(i=0;i<=w;i++)
		{
			LCD_CtrlWrite_Color(color);
		}
	}
	LCD_CrtlWrite_Area(Start_X,Start_X+LineW-(u8)1,Start_Y,End_Y);
	for (j=0; j<=h;j++)
	{
		for(i=0;i<LineW;i++)
		{
			LCD_CtrlWrite_Color(color);
		}
	}
	LCD_CrtlWrite_Area((End_X-LineW)+(u8)1,End_X,Start_Y,End_Y);
	for (j=0; j<=h;j++)
	{
		for(i=0;i<LineW;i++)
		{
			LCD_CtrlWrite_Color(color);
		}
	}
}

 //画实体方块
void Draw_Rectangle_Real(uint8_t Start_X, uint16_t Start_Y,uint8_t End_X, uint16_t End_Y,uint8_t color)
{
	uint16_t i,j,w,h;
	w=(uint16_t)End_X-(uint16_t)Start_X;
	h=End_Y-Start_Y;
	LCD_CrtlWrite_Area(Start_X,End_X,Start_Y,End_Y);
	for (j=0; j<=h;j++)
	{
		for(i=0;i<=w;i++)
		{
			LCD_CtrlWrite_Color(color);
		}
	}
}

//显示单色图形
//X-坐标 0-239
//Y-坐标 0-319
//H-高   2-200
//W-宽   1-100
//gp 字符地址
//NUM-图形编号
//color-颜色
static void DISP_MONO_GRAP(uint8_t  X1, uint16_t Y1,uint8_t  H, uint8_t  W,const uint8_t *gp,uint8_t color)
{
	uint16_t w,j;
	LCD_CrtlWrite_Area(X1,X1+H-(u8)1,Y1,Y1+(u16)W-(u16)1);
	for(w=0;w<((u16)H*((u16)W)/(u16)8);w++)
	{
		for(j=0;j<(uint16_t)8;j++)
		{
			if((gp[w]&((uint16_t)0x01<<j))!=(u16)0)
			{
				LCD_CtrlWrite_Color(color);
			}
			else
			{
				LCD_CtrlWrite_Color(Back_Color);
			}	
		}
	}
}


