//m^n函数
//返回值:m^n次方.
static u32 LCD_Pow(u8 m,u8 n)
{
	u32 result=1;	 
	while(n-->(u8)0)
	{
		result*=m;
	}    
	return result;
}		


//显示数字,高位为0,还是显示
//x,y:起点坐标
//size:字体大小	 16或24	 
//len:长度(即要显示的位数)
//num:数值(0~999999999);
//mode:	  7和0位使用
//[7]:0,不填充;1,填充0.
//[6:1]:保留
//[0]:0,非叠加显示;1,叠加显示.
//color:颜色 
void LCD_ShowxNum(u16 x,u16 y,u8 size,u8 len,u32 num,u8 mode,u8 color)
{  
	u8 t,temp;
	u8 enshow=0;						   
	for(t=0;t<len;t++)
	{
		temp=(u8)((num/LCD_Pow((u8)10,(len-t)-(u8)1))%(u8)10);	 //求需要显示的最高位
		if((enshow==(u8)0)&&(t<(len-(u8)1)))// 如果最高位为0   需要显示的长度
		{
			if(temp==(u8)0)
			{
				if((mode&(u8)0X80)!=(u8)0)
				{
					LCD_ShowChar(x,y+((size/(u16)2)*t),size,(u8)'0',mode&(u8)0x01,color);  //为0也要显示
				}
				else
				{
					LCD_ShowChar(x,y+((size/(u16)2)*t),size,(u8)' ',mode&(u8)0x01,color);  //为0不显示
				}					
				continue;
			}
			else
			{
				enshow=1; //最高位不是0或	
			}				 		 	 
		}
	 	LCD_ShowChar(x,y+((size/(u16)2)*t),size,temp+(u8)'0',mode&(u8)0x01,color); 
	}
} 

