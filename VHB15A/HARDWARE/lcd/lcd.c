static void SdCmd(uint8_t data8)	 //2014-06-10 V1.04更换夏瑞的LCD屏，驱动由ILI9328变更为ILI9341
{
	LCD_RS_L;
	LCD_PORT =data8;
	LCD_CS_L;
	LCD_WR_L;
	LCD_WR_H;
	LCD_CS_H;
}


static void SdData(uint8_t data8)  //2014-06-10 V1.04更换夏瑞的LCD屏，驱动由ILI9328变更为ILI9341
{
	LCD_RS_H;
	LCD_PORT =data8;
	LCD_CS_L;
	LCD_WR_L;
	LCD_WR_H;
	LCD_CS_H;
}

void LCD_CtrlWrite_ILI9328(uint16_t i)
{
	LCD_CS_L;
	LCD_RS_H;
	LCD_PORT =(uint8_t)(i/(u16)256);
	LCD_WR_L;
	LCD_WR_H;
	LCD_PORT =(u8)(i%(u16)256);
	LCD_WR_L;
	LCD_WR_H;
	LCD_RS_H;
	LCD_CS_H;
}

